@implementation EditPrivateProfileTableViewController
@synthesize privateTableView;
- (void)viewDidLoad {
    [super viewDidLoad];
    self.privateTableView.tableFooterView = [[UIView alloc] initWithFrame:CGRectZero];
    types = @[@"My Phone Numbers", @"My Email Addresses"];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    // Return the number of rows in the section.
    return types.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    EditPrivateProfileTableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"privateCell" forIndexPath:indexPath];
    cell.separatorInset = UIEdgeInsetsZero;
    cell.typeLabel.text = types[indexPath.row];
    return cell;
}

//On click of cell, segue
- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView cellForRowAtIndexPath:indexPath];
    [cell setSelectionStyle:UITableViewCellSelectionStyleDefault];
    
    
    //Switch statement for segue to edit
    switch(indexPath.row)
    {
        case 0:
            [self performSegueWithIdentifier:@"phoneNumberSegue" sender:self];
            break;
        case 1:
            [self performSegueWithIdentifier:@"emailAddressSegue" sender:self];
            break;
        default:
            break;
    }
}

-(void)tableView:(UITableView *)tableView willDisplayCell:(UITableViewCell *)cell forRowAtIndexPath:(NSIndexPath *)indexPath
{
    // Remove seperator inset
    if ([cell respondsToSelector:@selector(setSeparatorInset:)]) {
        [cell setSeparatorInset:UIEdgeInsetsZero];
    }
    
    // Prevent the cell from inheriting the Table View's margin settings
    if ([cell respondsToSelector:@selector(setPreservesSuperviewLayoutMargins:)]) {
        [cell setPreservesSuperviewLayoutMargins:NO];
    }
    
    // Explictly set your cell's layout margins
    if ([cell respondsToSelector:@selector(setLayoutMargins:)]) {
        [cell setLayoutMargins:UIEdgeInsetsZero];
    }
}

- (CGFloat)tableView:(UITableView *)tableView heightForHeaderInSection:(NSInteger)section {
    return 0.1f;
}
